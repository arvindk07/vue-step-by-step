<template>
  <h1>Watcher</h1>
  <h1>{{ count }}</h1>
  <button v-on:click="count = count + 1">+</button>
  <button v-on:click="count = count - 1">-</button>
</template>

<script>
export default {
  name: "HomeComponents",
  data() {
    return {
      count: 0,
    };
  },
  watch: {
    count(val, prev) {
      if (val > 5 && val > prev) {
        // alert("stop counting");
        this.count = 0;
      }
    },
  },
};
</script>

<style scoped>
h1 {
  color: purple;
}
</style>
